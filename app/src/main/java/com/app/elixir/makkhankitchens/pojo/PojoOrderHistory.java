public class PojoOrderHistory {

    String orderTilte;
    String orderAddress;
    String orderPhoneNo;
    String orderPrice;

    public String getOrderTilte() {
        return orderTilte;
    }

    public void setOrderTilte(String orderTilte) {
        this.orderTilte = orderTilte;
    }

    public String getOrderAddress() {
        return orderAddress;
    }

    public void setOrderAddress(String orderAddress) {
        this.orderAddress = orderAddress;
    }

    public String getOrderPhoneNo() {
        return orderPhoneNo;
    }

    public void setOrderPhoneNo(String orderPhoneNo) {
        this.orderPhoneNo = orderPhoneNo;
    }

    public String getOrderPrice() {
        return orderPrice;
    }

    public void setOrderPrice(String orderPrice) {
        this.orderPrice = orderPrice;
    }
}
